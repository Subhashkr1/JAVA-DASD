//Q-3. How do you find the length of a string in Java?
//Ans: We can find the length of a string in Java using length() method. For example:

public class Problem3 {
    public static void main(String[] args) {
        String str = "Hello World";
        int len = str.length();
        System.out.println("Length of String is: " + len);
    }
    
}
