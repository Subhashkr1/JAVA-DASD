//Q-2. How do you concatenate two strings in Java?
//Ans: We can concatenate two strings in Java using + operator. For example:


public class Problem2 {
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = "World";
        String str3 = str1 + str2;
        System.out.println(str3);
    }
    
}
